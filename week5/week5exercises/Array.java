public class Array{
	public static void main(String[]args){
		int N = 1000; // determine the size of array
		double[] a; // declaring the array
		a = new double [N]; //array, a with size, N of 1000 has been created
		for (int i=0; i < N; ++i) // initialised the array with a for statement and added variable i, the position of array. 
			a[i] = 1000; // accessing the area of array we want 
	}
}